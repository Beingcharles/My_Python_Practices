def sub():
    print("subraction: ")
    a=int(input("enter a: "))
    b=int(input("enter b: "))
    print(a-b)

def mod():
    print("modulus: ")
    c=int(input("enter c: "))
    d=int(input("enter d: "))
    print(c%d)

def exp():
    print("exponential: ")
    e=int(input("enter e: "))
    f=int(input("enter f: "))
    print(e**f)

def mul():
    print("multiplication: ")
    g=int(input("enter g: "))
    h=int(input("enter h: "))
    print(g*h)

    
sub()
mod()
exp()
mul()
